/*
ШПАРГАЛКА ПО МЕТОДАМ И ОПЕРАЦИЯМ

1. split() - разбивает строку на массив строк на основе указанного разделителя.
Пример:
const data = "a,b,c".split(',');
// data: ["a", "b", "c"]

2. slice() - возвращает подмассив, начиная от указанного индекса до (но не включая) конечного индекса.
Пример:
const arr = [1, 2, 3, 4, 5].slice(1, 4);
// arr: [2, 3, 4]

3. map() - создаёт новый массив с результатом вызова указанной функции для каждого элемента массива.
Пример:
const doubled = [1, 2, 3].map(num => num * 2);
// doubled: [2, 4, 6]

4. filter() - создаёт новый массив со всеми элементами, прошедшими проверку указанной функции.
Пример:
const evens = [1, 2, 3, 4, 5].filter(num => num % 2 === 0);
// evens: [2, 4]

5. trim() - удаляет пробельные символы с обоих концов строки.
Пример:
const text = "  Hello  ".trim();
// text: "Hello"

6. parseFloat() - преобразует строку в число с плавающей точкой.
Пример:
const number = parseFloat("10.5");
// number: 10.5

7. parseInt() - анализирует строку и возвращает целое число.
Пример:
const integer = parseInt("10");
// integer: 10

8. forEach() - выполняет указанную функцию один раз для каждого элемента массива.
Пример:
[1, 2, 3].forEach(num => console.log(num));
// Выводит: 1 2 3

9. Infinity - специальное числовое значение, представляющее бесконечность.
Пример:
const bigNumber = 1 / 0;
// bigNumber: Infinity

*/